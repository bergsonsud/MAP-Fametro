    private void btn_criptografarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_criptografarActionPerformed
        // TODO add your handling code here:
               
        String valor = TextFieldVogal1.getText();
        boolean continuar = false;

        
        for (int i = 0; i < vogais.length; i++) {
            if (valor.equals(vogais[i])){
             continuar=true;   
            }         
           
        }
        
        if (continuar){
        int crip = Arrays.binarySearch(vogais, valor);        
        TextFieldCripto1.setText(String.valueOf(crip+1));
        }else{
            JOptionPane.showMessageDialog(null, "Valor invalido, Digite uma vogal");
            TextFieldVogal1.setText("");
            TextFieldCripto1.setText("");
        }
    }//GEN-LAST:event_btn_criptografarActionPerformed

